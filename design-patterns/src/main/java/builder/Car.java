package builder;

public class Car {
	private String autobody;
	private String wheels;
	
	
	public String getAutobody() {
		return autobody;
	}
	public void setAutobody(String autobody) {
		this.autobody = autobody;
	}
	public String getWheels() {
		return wheels;
	}
	public void setWheels(String wheels) {
		this.wheels = wheels;
	}

}